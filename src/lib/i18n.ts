export const DICTS: Record<LangCode, Dict> = {
  ko, en, ja, zh, "zh-TW": zhTW,
  es, pt, fr, de, it, nl,
  ru, uk, pl, cs, hu, ro, tr,
  th, vi, id, ms, tl,
  hi, bn, ta, te,
  ar, fa, sw,
};

export function getDict(lang: LangCode): Dict {
  return DICTS[lang] || DICTS.en;
}

const SUPPORTED: LangCode[] = [
  "ko","en","ja","zh","zh-TW",
  "es","pt","fr","de","it","nl",
  "ru","uk","pl","cs","hu","ro","tr",
  "th","vi","id","ms","tl",
  "hi","bn","ta","te",
  "ar","fa","sw",
];

export function normalizeLang(input?: string | null): LangCode {
  const raw = String(input || "en").toLowerCase();
  // Traditional vs Simplified Chinese.
  if (raw.startsWith("zh")) {
    if (raw.includes("tw") || raw.includes("hant") || raw.includes("hk") || raw.includes("mo")) {
      return "zh-TW";
    }
    return "zh";
  }
  // Filipino → Tagalog.
  if (raw.startsWith("fil")) return "tl";
  const base = raw.split("-")[0];
  return (SUPPORTED.includes(base as LangCode) ? base : "en") as LangCode;
}

/** Heuristic language detection from raw user text. */
export function detectLangFromText(text: string): LangCode | null {
  const t = String(text || "");
  // Scripts first — most reliable.
  if (/[\uAC00-\uD7AF\u1100-\u11FF]/.test(t)) return "ko";
  if (/[\u3040-\u309F\u30A0-\u30FF]/.test(t)) return "ja"; // hiragana/katakana
  if (/[\u4E00-\u9FFF]/.test(t)) return "zh"; // Han chars default to Simplified
  if (/[\u0E00-\u0E7F]/.test(t)) return "th";
  if (/[\u0900-\u097F]/.test(t)) return "hi";
  if (/[\u0980-\u09FF]/.test(t)) return "bn";
  if (/[\u0B80-\u0BFF]/.test(t)) return "ta";
  if (/[\u0C00-\u0C7F]/.test(t)) return "te";
  if (/[\u0600-\u06FF]/.test(t)) {
    if (/[\u067E\u0686\u0698\u06A9\u06AF\u06CC]/.test(t)) return "fa";
    return "ar";
  }
  if (/[\u0400-\u04FF]/.test(t)) {
    if (/[іїєґ]/i.test(t)) return "uk";
    return "ru";
  }
  // Latin-script heuristics.
  if (/\b(estoy|siento|solo|sola|miedo|ansioso|triste|necesito|ayuda|oración|gracias)\b/i.test(t)) return "es";
  if (/\b(estou|sinto|sozinho|sozinha|medo|triste|preciso|ajuda|oração|obrigado)\b/i.test(t)) return "pt";
  if (/\b(je|suis|peur|seul|seule|triste|besoin|aide|prière|merci)\b/i.test(t)) return "fr";
  if (/\b(ich|bin|angst|allein|traurig|brauche|hilfe|gebet|danke)\b/i.test(t)) return "de";
  if (/\b(sono|paura|solo|sola|triste|bisogno|aiuto|preghiera|grazie)\b/i.test(t)) return "it";
  if (/\b(saya|aku|takut|sendiri|sedih|butuh|tolong|doa)\b/i.test(t)) return "id";
  if (/\b(tôi|sợ|một mình|buồn|cần|giúp|cầu nguyện|cảm ơn)\b/i.test(t)) return "vi";
  if (/\b(jestem|boję|sam|sama|smutny|smutna|potrzebuję|pomoc|modlitwa|dziękuję)\b/i.test(t)) return "pl";
  if (/\b(korkuyorum|yalnız|üzgün|ihtiyacım|yardım|dua|teşekkür)\b/i.test(t)) return "tr";
  if (/[a-z]/i.test(t)) return "en";
  return null;
}
